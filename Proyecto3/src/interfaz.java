    public void iniciar() {
        int espacio;
        //numero aleatorios entre 10 y 3

        for (int i = 0; i < 10; i++) {
            espacio = (int) (Math.random() * (10 - 3 + 1) + 3);
            PrimerAjuste nuevo = new PrimerAjuste("h", admin.buscarindice(), espacio);
            admin.addContenidoInicial(nuevo);
        }
        LinkedList<PrimerAjuste> cont = admin.mandarLista();
        for (int i = 0; i < cont.size(); i++) {
            //String lista = cont.get(i).getTipo() + " " + cont.get(i).getEmpieza() + " l:" + cont.get(i).getLongitud();
            lista = admin.mandarInfo();
            modelo.addRow((Object[]) lista.get(i));
            int aux = cont.get(i).getLongitud() - 4;
            if (cont.get(i).getLongitud() < 5) {
                jTable1.setRowHeight(i, (30 + aux));
            } else {
                aux = cont.get(i).getLongitud() - 10;
                jTable1.setRowHeight(i, (50 + aux));
            }
        }
    }

    public void actualizarTabla() {
        //primero eliminar todas los procesos
        int tt = modelo.getRowCount();
        for (int i = tt - 1; i >= 0; i--) {
            modelo.removeRow(i);
        }
        //luego actualizar la memoria con los nuevos
        lista.clear();
        lista = admin.mandarInfo();
        LinkedList<PrimerAjuste> cont = admin.mandarLista();
        for (int i = 0; i < lista.size(); i++) {
            modelo.addRow((Object[]) lista.get(i));
            int aux = cont.get(i).getLongitud() - 4;
            if (cont.get(i).getLongitud() < 5) {
                jTable1.setRowHeight(i, (30 + aux));
            } else {
                aux = cont.get(i).getLongitud() - 10;
                jTable1.setRowHeight(i, (50 + aux));
            }

            //System.out.println("TamaÃ±oo  :"+ t.get(i));
        }

    }

